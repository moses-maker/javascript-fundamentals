/*
- Number data type is used to represent numbers
- Examples
    Integers
    decimals
    exponentials 
    octal
    hexadecimal
    binary
*/

let integerNumber = 100; // these are whole numbers without decimal.
let decimalNumber = 78.45; // numbers with decimals
let exponentialNumber = 1.4e15;
let octalNumber = 0o10; // decimal version would be 8
let hexadecimalNumber = 0x3E8; // decimal version would be 1000
let binaryNumber = 0b101; //decimal version would be 5

console.log(integerNumber, decimalNumber, exponentialNumber, octalNumber);
console.log(hexadecimalNumber, binaryNumber);

