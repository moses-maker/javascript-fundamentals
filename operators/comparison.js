// equal == , ===
let x = 5;  // integer
let y = "5"; // string
console.log(x == y);
console.log(x === y);

let j = 60;
let k = 60;
console.log(j == k);

// not equal !=, !==
let a = 7;
let b = 4;
console.log(a != b);

let c = 8;
let d = "8";
console.log(c !== d);

// greater than >, greater than or equal to >=
let e = 10;
let f = 10;
console.log(e > f);
console.log(e >= f);

// less than <, less than or equal to <=
let g = 9;
let h = 9;
console.log(g < h);
console.log(g <= h);







