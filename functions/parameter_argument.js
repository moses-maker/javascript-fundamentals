/* parameter
- variable listed inside the parenthesis of the function definition
- they are declared like so:

function myFunc(param1, param2){
    // code of the function
}

*/

function addTwoNumbers(x, y){
    console.log(x + y);
}

/*
- When called, this function will simply add parameters and log the result.
- However, to do this we can call the function with two arguments
*/
addTwoNumbers(3);
addTwoNumbers(12, -90);